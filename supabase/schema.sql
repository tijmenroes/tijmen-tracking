-- ------------------------------------------------------------
-- exercise_sets
-- Individual sets within a workout_exercise entry.
-- Strength fields: weight_kg, reps
-- Endurance fields: duration_seconds, distance_km
-- ------------------------------------------------------------
create table if not exists exercise_sets (
  id                  bigint generated by default as identity primary key,
  workout_exercise_id bigint not null references workout_exercises (id) on delete cascade,
  set_number          int not null default 1,
  weight_kg           real,
  reps                int,
  duration_seconds    int,
  distance_km         real,
  created_at          timestamptz not null default now()
);

alter table exercise_sets enable row level security;

-- Access is granted transitively via workouts ownership
create policy "Users can read own exercise_sets"
  on exercise_sets for select
  using (exists (
    select 1 from workout_exercises we
    join workouts w on w.id = we.workout_id
    where we.id = workout_exercise_id and w.user_id = auth.uid()
  ));

create policy "Users can insert own exercise_sets"
  on exercise_sets for insert
  with check (exists (
    select 1 from workout_exercises we
    join workouts w on w.id = we.workout_id
    where we.id = workout_exercise_id and w.user_id = auth.uid()
  ));

create policy "Users can update own exercise_sets"
  on exercise_sets for update
  using (exists (
    select 1 from workout_exercises we
    join workouts w on w.id = we.workout_id
    where we.id = workout_exercise_id and w.user_id = auth.uid()
  ));

create policy "Users can delete own exercise_sets"
  on exercise_sets for delete
  using (exists (
    select 1 from workout_exercises we
    join workouts w on w.id = we.workout_id
    where we.id = workout_exercise_id and w.user_id = auth.uid()
  ));
